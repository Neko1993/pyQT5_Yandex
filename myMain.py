import sys

from PyQt5 import uic
from PyQt5.QtWidgets import (QApplication, QWidget, QPushButton, QLabel, QLineEdit, QGridLayout, QMessageBox,
                             QMainWindow)

import DB.tools as DB


class LoginForm(QWidget):
    def __init__(self):
        super().__init__()
        uic.loadUi('forms/LoginWidget.ui', self)
        self.setFixedSize(300, 200)
        self.login_btn.clicked.connect(self.check_password)

    def check_password(self):
        conn = DB.connect()
        ans = DB.check_login(conn, self.login_text.text(), self.password_text.text())

        msg = QMessageBox()

        if ans is not None:
            msg.setText('Добро пожаловать,  '+ans[0]+'!')
            msg.exec_()
        else:
            msg.setText('Данные введены не верно')
            msg.exec_()


if __name__ == '__main__':
    app = QApplication(sys.argv)

    form = LoginForm()
    form.show()

    sys.exit(app.exec_())
